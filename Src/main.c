/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	MCP_Handle_t	*pMCP;

	uint8_t	ret;
	
	//- - - CAN - - -
/*	
	CAN_TxHeaderTypeDef	TxMsg;
	uint8_t					TxData[8]={0x011,0x22,0x33,0x44,0x55,0x66,0x77,0x88};
	uint32_t				TxFIFONum;
	CAN_SetMsg(&TxMsg);
*/
	
	CO_NMT_reset_cmd_t reset;
	CO_ReturnError_t err;
	uint16_t timer1msPrevious;
	uint16_t timer1msCopy, timer1msDiff;
  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
	

  reset = CO_RESET_NOT;
  timer1msPrevious = CO_timer1ms;

		
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_DAC_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_USART3_UART_Init();
  MX_MotorControl_Init();
  MX_USART1_UART_Init();
  MX_CAN_Init(&hcan,1000);

  /* Initialize interrupts */
  MX_NVIC_Init();
	
  /* USER CODE BEGIN 2 */
	pMCP = GetMCP();									//get	MCP instance
	/* usart 1 initialize */
	bool	u1;
	U1FCP_Init( &pUSART1 );
   // pMCP = &U1MCP_UI_Params;
   // MCP_Init(pMCP, (FCP_Handle_t *) & pUSART1, MC_NULL, MC_NULL, MC_NULL, pDAC, s_fwVer);
	pUSART1._Super.RxFrameState	=	FCLP_TRANSFER_IDLE;
	ModBus_CFGInit();
	LL_USART_EnableIT_RXNE(USART1);   // startup uart1 receive interrupt
	LL_USART_EnableIT_PE(USART1);  
		
		/* initialize CANopen */
		reset = CO_RESET_NOT;
    err = CO_init((int32_t)&hcan , 5, 1000,&pMCP->_Super);		/*( CAN module address , NodeID , bit rate,UI_Handle_t ) */
    if(err != CO_ERROR_NO){
            while(1);
            /* CO_errorReport(CO->em, CO_EM_MEMORY_ALLOCATION_ERROR, CO_EMC_SOFTWARE_INTERNAL, err); */
    }
  CO_CANsetNormalMode(CO->CANmodule[0]);		 /* start CAN */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
		 u1	=	UI_Serial1CommunicationTimeOutHasElapsed();
		if(u1 ){//uart1 receive data timeout,has already frame data.
			LL_USART_DisableIT_RXNE(USART1);
			//memcpy((char*)&mBaseHandle,(char*)&pUSART1._Super,sizeof(mBaseHandle));

			pBaseHandle	=	&mBaseHandle;
			ret=	RtuModbusParse(pMCP, 	pUSART1._Super.RxFrame.Buffer,
																	pUSART1._Super.TxFrame.Buffer,
																	&pUSART1._Super.RxFrame.Size,
																		FCLP_MAX_PAYLOAD_SIZE );
			if(ret == MB_NO_ERR){
					pUSART1._Super.TxFrameLevel = 0;
					U1FCP_Send(&pUSART1._Super,0,pUSART1._Super.TxFrame.Buffer,pUSART1._Super.RxFrame.Size);
				}
			pUSART1._Super.RxFrame.Size	=	0;
			pUSART1._Super.RxFrameLevel	=	0;
			LL_USART_EnableIT_RXNE(USART1);   // restart uart1 receive interrupt
			}
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
			CO_tmr_Task_thread();
			
			timer1msCopy = CO_timer1ms;
      timer1msDiff = timer1msCopy - timer1msPrevious;
      timer1msPrevious = timer1msCopy;
            /* CANopen process */
      reset = CO_process(CO, timer1msDiff, NULL);
			
/*			
			if ( CAN_RxStatus == 'R'){
				CAN_RxStatus	=	0;
				memcpy(TxData,RxData,sizeof(TxData));
				TxMsg.StdId	=	RxMsg.StdId; 
				HAL_CAN_AddTxMessage(&hcan,&TxMsg,TxData,&TxFIFONum);
			}
*/
			
  }
  /* USER CODE END 3 */

}

